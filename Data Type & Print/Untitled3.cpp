#include <stdio.h>

int main(){
	printf("정수 %d과 %d\n", 153, -153);
	printf("실수 %.3f와 %.3f\n", 42.195, -42.195);
	printf("문자 '%c'와 '%c'\n", 'A', 'a');
	printf("문자열 \"%s\"와 \"%s\"\n", "Artificial Intelligence", "마빈 민스키");
	printf("자연수 %d를 %d진수, %o진수, %x진수로 출력해 봅시다.", 255, 255, 255, 255);
	return 0;
}
